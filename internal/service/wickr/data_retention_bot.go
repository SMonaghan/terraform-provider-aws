// Design choice: SNS email subscription pattern for enabled/pubkey_msg_acked.
//
// The Wickr data retention bot has a two-phase setup that mirrors
// aws_sns_topic_subscription with protocol="email":
//
//  1. Terraform creates the bot and generates the challenge password.
//  2. The user configures their external data-retention daemon with the
//     challenge password (out-of-band, like clicking an SNS confirmation link).
//  3. The daemon connects and registers with the bot.
//  4. Only after registration does the UpdateDataRetention API accept
//     ENABLE/DISABLE/PUBKEY_MSG_ACK actions.
//
// Because step 3 is an out-of-band process that Terraform cannot drive,
// the `enabled` and `pubkey_msg_acked` attributes are Computed-only —
// they reflect the current state reported by GetDataRetentionBot, just
// like `confirmation_was_authenticated` on an SNS email subscription.
// Users manage the enable/disable toggle via the AWS CLI or console
// after their daemon has registered. `terraform refresh` picks up the
// updated state.
//
// The `challenge` attribute follows the aws_iam_access_key.secret pattern:
// a one-time-generated credential stored as Computed+Sensitive in state.

// @FrameworkResource("aws_wickr_data_retention_bot", name="Data Retention Bot")
// @IdentityAttribute("network_id")
// @Testing(existsType="github.com/aws/aws-sdk-go-v2/service/wickr;wickr.GetDataRetentionBotOutput")
// @Testing(preCheck="testAccPreCheck")
// @Testing(serialize=true)
// @Testing(tagsTest=false)
// @Testing(hasNoPreExistingResource=true)
func newDataRetentionBotResource(_ context.Context) (resource.ResourceWithConfigure, error) {
	r := &dataRetentionBotResource{}

	r.SetDefaultCreateTimeout(30 * time.Minute)
	r.SetDefaultReadTimeout(10 * time.Minute)
	r.SetDefaultUpdateTimeout(30 * time.Minute)
	r.SetDefaultDeleteTimeout(30 * time.Minute)

	return r, nil
}

