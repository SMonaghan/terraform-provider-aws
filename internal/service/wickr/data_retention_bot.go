// isDataRetentionBotOrphanedChildError returns true when err indicates the
// data retention bot (or its parent network) is gone. Mirrors
// isBotOrphanedChildError.
func isDataRetentionBotOrphanedChildError(err error) bool {
	if err == nil {
		return false
	}
	if errs.IsA[*awstypes.ForbiddenError](err) {
		return true
	}
	msg := err.Error()
	return strings.Contains(msg, "StatusCode: 401") ||
		strings.Contains(msg, "deserialization failed")
}

// flattenDataRetentionBot populates the model from a GetDataRetentionBotOutput.
// AutoFlex cannot be used here because the SDK output field names
// (BotExists, BotName, IsBotActive, IsDataRetentionBotRegistered,
// IsDataRetentionServiceEnabled, IsPubkeyMsgAcked) do not match the
// Terraform schema attribute names (bot_exists, bot_name, is_bot_active,
// is_data_retention_bot_registered, enabled, pubkey_msg_acked). The last
// two are intentional renames for user-friendliness.
// nosemgrep:ci.semgrep.framework.manual-flattener-functions
func flattenDataRetentionBot(ctx context.Context, out *wickr.GetDataRetentionBotOutput, m *dataRetentionBotResourceModel) {
	if out == nil {
		return
	}

	m.BotExists = fwflex.BoolToFramework(ctx, out.BotExists)
	m.BotName = fwflex.StringToFramework(ctx, out.BotName)
	m.IsBotActive = fwflex.BoolToFramework(ctx, out.IsBotActive)
	m.IsDataRetentionBotRegistered = fwflex.BoolToFramework(ctx, out.IsDataRetentionBotRegistered)
	m.Enabled = fwflex.BoolToFramework(ctx, out.IsDataRetentionServiceEnabled)
	m.PubkeyMsgAcked = fwflex.BoolToFramework(ctx, out.IsPubkeyMsgAcked)
}

